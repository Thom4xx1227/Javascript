

alert ("bienvenido a la calculadora")

let numero1 = parseInt(prompt(`ingrese su primer numero`)); 
let numero2 = parseInt(prompt(`ingrese su segundo numero`));

let suma = numero1 + numero2
let resta = numero1 - numero2
let modulo = numero1 % numero2
let producto = numero1 * numero2 
let cociente = numero1 / numero2





alert (`
    resultado de la suma = ${suma} 
    resultado de la resta = ${resta}  
    resultado de su modulo = ${modulo} 
    resultado de su producto = ${producto} 
    resultado de su cociente =  ${cociente}`
);